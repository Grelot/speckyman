/***********************************************************************************************************************************************************************************

	PPlayer.ANIMATIONS

***********************************************************************************************************************************************************************************/

PPlayer.prototype.animations = function()
{
	if ( this.left || this.right ) 
	{ 
		this.sprite.pos.orientation = this.left ? "Gauche" : "Droite";
		this.sprite.maj( this.left ? "Gauche" : "Droite", { y : 1, x : 0 }, 8); 
	}
	else					{ this.sprite.maj("Neutre", { y : 0, x : 0 }); }
}