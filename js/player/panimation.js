/***********************************************************************************************************************************************************************************

	PPLAYER

***********************************************************************************************************************************************************************************/

function PPlayer(pos)
{
	var size = { width : 160, height : 160 };
	PCharacter(this, pos, size); // On ajoute les propriétés propres à un Character

	this.type = "Player";
	this.player = 0;
	this.pad = 0;

	this.uniforme = false;
	this.v.s = 8; // 20;

	this.sprite = new Sprite( img["char"]["costa_walk"], { x : 8, y : 2 } );
	this.sprite.zoom = 4;

	this.hitbox = new PHitbox( { width : 5, height : 5 }, {});
}

/***********************************************************************************************************************************************************************************

	PPlayer.CONTROLLER

***********************************************************************************************************************************************************************************/

PPlayer.prototype.controller = function()
{
	this.actions("Reset");


	if ( PAD("DOWN", this.pad) ) // Avant tout, on regarde si le joueur est baissé
		{ this.down = true; }
	else if ( PAD("UP", this.pad) ) // Ou regarde en haut
		{ this.up = true; }
	if ( PAD("RIGHT", this.pad) )
		{ this.right = this.wall.r ? false : true; }
	else if ( PAD("LEFT", this.pad) )
		{ this.left = this.wall.l ? false : true; }

	if ( this.right )	{ this.actions("Droite"); }
	if ( this.left )	{ this.actions("Gauche"); }


}

/***********************************************************************************************************************************************************************************

	PPlayer.ANIMATE

***********************************************************************************************************************************************************************************/

PPlayer.prototype.animate = function()
{
	this.wall.l = false;
	this.wall.r = false;

	this.controller();
	this.physics();
	this.timers();
}

